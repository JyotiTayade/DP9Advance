
// Declare variables of different data types
let numVar: number = 10;
let strVar: string = "Hello, TypeScript!";
let boolVar: boolean = true;
let undefinedVar: undefined = undefined;

// Perform basic operations
let resultAddition: number = numVar + 5;
let resultConcatenation: string = strVar + " Welcome!";
let resultNegation: boolean = !boolVar;

// Print the values and results
console.log("Number Variable:", numVar);
console.log("String Variable:", strVar);
console.log("Boolean Variable:", boolVar);
console.log("Undefined Variable:", undefinedVar);

console.log("\nPerforming Operations:");

console.log("Addition Result:", resultAddition);
console.log("Concatenation Result:", resultConcatenation);
console.log("Negation Result:", resultNegation);
